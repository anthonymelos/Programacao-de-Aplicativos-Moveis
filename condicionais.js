/* FIXME: Condicional

A estrutura básica para condições em JavaScript é o if, else if, e else. 
Eles permitem executar blocos de código diferentes com base na avaliação 
de uma condição.

*/

let idade = 18;

if (idade < 18) {
    console.log("Você é menor de idade.");
} else if (idade === 18) {
    console.log("Você acabou de atingir a maioridade.");
} else {
    console.log("Você é maior de idade.");
}