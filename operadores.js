/* FIXME: Operadores de comparação

==: Igual a
===: Estritamente igual a
!=: Diferente de
!==: Estritamente diferente de
>: Maior que
<: Menor que
>=: Maior ou igual a
<=: Menor ou igual a

*/

let a = 10;
let b = 20;

console.log(a + b); // Imprime: 30
console.log(a > b); // Imprime: false
console.log(a == 10); // Imprime: true
console.log(a === "10"); // Imprime: false