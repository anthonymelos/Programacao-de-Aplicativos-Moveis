/* FIXME: Swite casa

O switch é uma estrutura de controle que permite testar uma variável 
contra vários valores possíveis e executar o código correspondente.

*/

let dia = 3;

switch (dia) {
    case 1:
        console.log("Segunda-feira");
        break;
    case 2:
        console.log("Terça-feira");
        break;
    case 3:
        console.log("Quarta-feira");
        break;
    default:
        console.log("Outro dia da semana");
}